function bg(){
	let color = prompt("rojo, azul o amarillo?");
        if (color == "azul") {
          document.body.style.backgroundColor = "#0000FF";
        }
        if (color == "rojo") {
          document.body.style.backgroundColor = "#FF0000";
        }
        if(color=="amarillo"){
          document.body.style.backgroundColor = "#F0FF00";
        }
        
}
